package bot.utility;

public class DescAndURL {
    private final String desc;
    private final String url;

    public DescAndURL(String desc, String url) {
        this.desc = desc;
        this.url = url;
    }

    public String getDesc() {
        return this.desc;
    }

    public String getURL() {
        return this.url;
    }
}
